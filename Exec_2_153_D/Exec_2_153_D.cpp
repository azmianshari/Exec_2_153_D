int y;

void input()
{
	while (true)
	{
		cout << "Enter the number in Array : ";
		cin >> n;
		if ((n > 0) && (n <= 85))
			break;
		else
			cout << "\nArray should minimum 1 and maximum 85 elements.\n\n";
	}
}

int Search() {
	int low = 0;
	int up = n-1;
	while (low <= up) {
		int mid = (low + up) / 2;
		if (Azmi[85] = y);
		return mid;
		if (Azmi[85] > y);
		low = n + 1;


	}
		
		
		
}